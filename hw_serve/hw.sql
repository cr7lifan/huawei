#用户基本信息，用户
CREATE TABLE hw_user(
	uid INT PRIMARY KEY AUTO_INCREMENT,
	uname VARCHAR(32),
	upwd VARCHAR(32),
	email VARCHAR(64),
	phone VARCHAR(16),
	avatar VARCHAR(128),        #头像图片路径
	user_name VARCHAR(32),      #用户名，如王小明
	gender INT                  #性别  0-女  1-男
);

#收货地址信息，用户
CREATE TABLE hw_receiver_address(
	aid INT PRIMARY KEY AUTO_INCREMENT,
	user_id INT,                #用户编号
	receiver VARCHAR(16),       #接收人姓名
	province VARCHAR(16),       #省
	city VARCHAR(16),           #市
	county VARCHAR(16),         #县
	address VARCHAR(128),       #详细地址
	cellphone VARCHAR(16),      #手机
	fixedphone VARCHAR(16),     #固定电话
	postcode CHAR(6),           #邮编
	tag VARCHAR(16),            #标签名
	is_default BOOLEAN,          #是否为当前用户的默认收货地址
	FOREIGN KEY(user_id) REFERENCES hw_user(uid)
);

#购物车条目表，商品购买
CREATE TABLE hw_shoppingcar_item(
	iid INT PRIMARY KEY AUTO_INCREMENT,
	user_id INT,      #用户编号
	product_family_id INT,   #商品所属类别编号
	count INT,        #购买数量
	is_checked BOOLEAN,      #是否已勾选，确定购买
	FOREIGN KEY(user_id) REFERENCES hw_user(uid),
	FOREIGN KEY(product_family_id) REFERENCES hw_product_family(pid)
);

#用户订单表，记载所有的商品种类，商品购买
CREATE TABLE hw_order(
	aid INT PRIMARY KEY AUTO_INCREMENT,
	user_id INT,
	address_id INT,
	status INT,             #订单状态  1-等待付款  2-等待发货  3-运输中  4-已签收  5-已取消
	order_time BIGINT,      #下单时间
	pay_time BIGINT,        #付款时间
	deliver_time BIGINT,    #发货时间
	received_time BIGINT,    #签收时间
	FOREIGN KEY(user_id) REFERENCES hw_user(uid),
	FOREIGN KEY(address_id) REFERENCES hw_receiver_address(aid)
);

#用户订单详情表，商品购买
CREATE TABLE hw_order_detail(
	did INT PRIMARY KEY AUTO_INCREMENT,
	order_id INT,           #订单编号
	product_family_id INT,         #产品编号
	count INT,               #购买数量
	FOREIGN KEY(order_id) REFERENCES hw_order(aid),
	FOREIGN KEY(product_family_id) REFERENCES hw_product_family(pid)
);



