SET NAMES UTF8;
DROP DATABASE IF EXISTS hw;
CREATE DATABASE hw CHARSET=UTF8;
USE hw;

#商品类别表
CREATE TABLE hw_product_family(
	pid int primary key auto_increment,
	pname varchar(32)
);


#手机表，商家
CREATE TABLE hw_phone(
	pid INT PRIMARY KEY AUTO_INCREMENT,
	family_id INT,
	title VARCHAR(128),    #标题
	subtitle VARCHAR(128),	#副标题
	price DECIMAL(10,2),   #价格
	sale VARCHAR(256),   #促销
	explains VARCHAR(256),   #服务说明
	encode VARCHAR(15),   #产品编码
	color VARCHAR(12),   #颜色
	spec VARCHAR(32),   #版本
	promise VARCHAR(64),   #保障服务
	FOREIGN KEY(family_id) REFERENCES hw_product_family(pid)
);

#手机图片，商家
CREATE TABLE hw_phone_pic(
	pid INT PRIMARY KEY AUTO_INCREMENT,
	phone_id INT,
	sm VARCHAR(128),
	big VARCHAR(128),
	FOREIGN KEY(phone_id) REFERENCES hw_phone(pid)
);

